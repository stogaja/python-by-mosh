# the code below returns a floating point number after division

print(10 / 3)

# the code below returns an integer after division

print(10 // 3)

# modulus returns the remainder after division as shown below

print(10 % 3)

# exponentiation raises the integer to the desired power as shown below

print(10 ** 3)

# augmented assignment operator

x = 10
x = x + 3
print(x)

# augmented operator is shown below

x += 3

print(x)