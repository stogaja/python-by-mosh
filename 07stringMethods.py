course = 'python for beginners'

# counts the number of character input

print(len(course))

# converts letters into uppercase

print(course.upper())

# converts text into lowercase

print(course.lower())

# finding a character in a string
# find is a case sensitive method
# replace method replaces the chosen characters with the preferred input

print(course.find('s'))

# checking for presence of a string in a variable

print('python' in course)

